size_t	ft_strlcpy(char *c, const char *d, size_t len)
{
	int	n;

	n = 0;
	while (c[n])
		n++;
	if (len > n)
		len = n;
	n = 0;
	if (len > 0)
	{
		while (n < (len -1) && d[n] != '\0')
		{
			c[n] = d[n];
			n++;
		}
		c[n] = '\0';
	}
	n = 0;
	while (d[n])
		n++;
	return (n);
}
