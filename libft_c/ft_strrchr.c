char	*strrchr(const char *s, int c)
{
	int	n;

	n = 0;
	while (s[n])
		n++;
	/*if (c == 0)
		return (n);*/
	while (s[n] != c && n >= 0)
		n--;
	if (s[n] == c)
		return (n);
	else
	{
		return (NULL);
	}
}
